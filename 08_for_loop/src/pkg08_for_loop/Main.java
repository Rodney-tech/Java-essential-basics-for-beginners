package pkg08_for_loop;

/**
 * print 1 to 10 using for loop
 */
public class Main {

    public static void main(String[] args) {
        

	//repeats the same code couple of times based on the iteration, couting from 0
        for(int i = 0; i <= 9; i++){
            System.out.println(i+1);
        }
    }
    
}
